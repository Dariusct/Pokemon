package Combat;

/**
 * The enum Stat.
 * Represent the combat values of Monsters.
 * @author  uvzab
 */
public enum Stat {
    /**
     * Atk stat.
     */
    ATK,
    /**
     * Def stat.
     */
    DEF,
    /**
     * Spd stat.
     */
    SPD,
    /**
     * Prc stat.
     */
    PRC,
    /**
     * Agl stat.
     */
    AGL
}
